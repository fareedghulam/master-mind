export const pakistanBondDraws: PakistanBondDraw[] = rawData
  .split('\n')
  .map((line, i) => {
    const tokens = line.trim().split(/\s+/);
    if (tokens.length < 5) return null;

    const drawNoRaw = tokens[0];
    const value = tokens[1];
    const rawDate = tokens[2];
    const cityCode = tokens[3].toUpperCase();
    const firstPrize = tokens[4];
    const secondPrizes = tokens.slice(5);

    const drawIndex = parseInt(drawNoRaw, 10);
    const year = 2000 + Math.floor((drawIndex - 1) / 4);

    const dateParts = rawDate.split('/');
    let dateStr = `${year}-01-01`;
    if (dateParts.length === 2) {
      const day = dateParts[0].padStart(2, '0');
      const month = dateParts[1].padStart(2, '0');
      dateStr = `${year}-${month}-${day}`;
    }

    const cityUrdu = cityMap[cityCode] || cityCode;
    const bondNameUrdu = valueMap[value] || `${value} Bond`;

    return {
      id: `pk-draw-${drawIndex}-${value}`,
      category: 'pakistan_bond' as const,
      drawNo: `ڈرا نمبر ${drawIndex} (${bondNameUrdu})`,
      firstPrize,
      secondPrizes,
      date: dateStr,
      city: cityUrdu
    };
  })
  .filter((item): item is PakistanBondDraw => item !== null)
  .reverse(); // Reverse to put newest records (Year 2018/2017) first
